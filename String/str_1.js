const message = "Hello, JavaScript!";

console.log(message.length); // Output: 18 (counts characters including spaces)
console.log(message.toUpperCase()); // Output: HELLO, JAVASCRIPT!
console.log(message.toLowerCase()); // Output: hello, javascript!
console.log(message.includes("JavaScript")); // Output: true
console.log(message.replace("JavaScript", "JS")); // Output: Hello, JS!
