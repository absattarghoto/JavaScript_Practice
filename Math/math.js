console.log(Math.PI); // Output: 3.141592653589793
console.log(Math.abs(-10)); // Output: 10 (absolute value)
console.log(Math.sqrt(25)); // Output: 5 (square root)
console.log(Math.pow(2, 3)); // Output: 8 (2^3)
console.log(Math.round(4.6)); // Output: 5 (rounding)
console.log(Math.floor(4.9)); // Output: 4 (rounds down)
console.log(Math.ceil(4.1)); // Output: 5 (rounds up)
console.log(Math.max(10, 5, 20)); // Output: 20 (largest number)
console.log(Math.min(10, 5, 20)); // Output: 5 (smallest number)
