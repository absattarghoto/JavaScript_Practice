// Object
let person = { name: "Abdul", age: 25, country: "Pakistan" };

// Array
let colors = ["red", "blue", "green"];

// Function
function greet() {
    return "Hello, World!";
}

console.log(typeof person);  // "object"
console.log(typeof colors);  // "object" (arrays are special objects)
console.log(typeof greet);   // "function"
